package com.yusuke.entities;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author yusuke
 */
@Entity
@Table(name = "namelist")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Namelist.findAll", query = "SELECT n FROM Namelist n")
    , @NamedQuery(name = "Namelist.findById", query = "SELECT n FROM Namelist n WHERE n.id = :id")
    , @NamedQuery(name = "Namelist.findByName", query = "SELECT n FROM Namelist n WHERE n.name = :name")})
public class Namelist implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "ID")
    private Integer id;
    @Column(name = "Name")
    private String name;

    public Namelist() {
    }

    public Namelist(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Namelist)) {
            return false;
        }
        Namelist other = (Namelist) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.yusuke.inputdata.Namelist[ id=" + id + " ]";
    }
    
}
